acc_x_mean <- sapply(x,acc_x_mean_function)
acc_y_mean_function <- function(x){mean(json_data4$dataset[[x]]$accelerometer$y)}
acc_y_mean <- sapply(x,acc_y_mean_function)
acc_z_mean_function <- function(x){mean(json_data4$dataset[[x]]$accelerometer$z)}
acc_z_mean <- sapply(x,acc_z_mean_function)
